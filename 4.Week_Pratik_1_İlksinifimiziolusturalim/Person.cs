namespace _4.Week_Pratik_1_İlksinifimiziolusturalim
{
    public class Person
    {
        public string _name;
        public string _surname;
        public DateTime _date;

        public string Name
        {
            get
            {
                return _name;
            }
            set
            {
                _name = value;
            }
        }


        public string Surname
        {
            get
            {
                return _surname;
            }
            set
            {
                _surname = value;
            }
        }
        public DateTime Date
        {
            get
            {
                return _date;
            }
            set
            {
                _date = value;
            }
        }


    }
}
